    private void jButton_RemoveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_RemoveActionPerformed
        if(jTextField_id.getText().equals("")){
            JOptionPane.showMessageDialog(null, "Please select a Student to remove");
        } else {
        int id = Integer.valueOf(jTextField_id.getText());
        stu.insertUpdateDeleteStudent('d', id, null, null, null, null, null); // Delete the student record
        
        // Remove the previous students details from the fields
        jTextField_id.setText("");
        jTextField_forename.setText("");
        jTextField_surname.setText("");
        jTextField_stuno.setText("");
        
        //Disable the Gender buttons to remove the data
        jRadioButton_FT.setSelected(false);
        jRadioButton_PT.setSelected(false);
        
        //Remove the Date from the Field
        jDateChooser_bday.setDate(null);
        
        
        }
        // Update the table after removing
        jTable1.setModel(new DefaultTableModel (null, new Object[]{"ID","FORENAME", "SURNAME", "STUDENTNO","FULLPARTTIME","BIRTHDATE"}));
        stu.fillStudentJtable(ManageStudentForm.jTable1,"");
        
        // Update the amount of Students registered on the main page
        MainForm.jLabel_NoStd.setText("No of Students: "+Integer.toString(Count.countData("STUDENT")));
        
    }//GEN-LAST:event_jButton_RemoveActionPerformed

