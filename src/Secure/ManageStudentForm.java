    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        // When the user has selected a Student
        
        int rowNo = jTable1.getSelectedRow();
        DefaultTableModel tm = (DefaultTableModel)jTable1.getModel();
        
       jTextField_id.setText(tm.getValueAt(rowNo, 0).toString()); // Update the Student ID into the ID box
       jTextField_forename.setText(tm.getValueAt(rowNo, 1).toString()); // Update the Forename into the field
       jTextField_surname.setText(tm.getValueAt(rowNo, 2).toString()); // Update the surname into the field
       jTextField_stuno.setText(tm.getValueAt(rowNo, 3).toString()); // Update the Phone number into the field

       
       if(tm.getValueAt(rowNo, 4).toString().equals("F")){
           jRadioButton_FT.setSelected(true); // Select Male
       }else{
           jRadioButton_PT.setSelected(true); // Turn the Female Button on
       }
       
       // Updating the birth date from a date back into the field
       Date bday; // Enable bday
       
        try {
            bday = new SimpleDateFormat("yyyy-MM-dd").parse(tm.getValueAt(rowNo, 5).toString());
            jDateChooser_bday.setDate(bday);
        } catch (ParseException ex) {
            Logger.getLogger(ManageStudentForm.class.getName()).log(Level.SEVERE, null, ex);
        }
       
    
        
    }//GEN-LAST:event_jTable1MouseClicked

